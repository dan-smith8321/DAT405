//Initialization function
function setup() {
  //Set the size of rendering window - pixels
  createCanvas(400, 400);
  fill(255,224,56)


  //No stroke for shapes
  noStroke();
}

//Rendering function
function draw() {
  background(55,51,46);
  ellipse(10,10, 90, 90);
    rect(10, 10, 1, 400);
  strokeWeight(8); // Stroke weight to 8 pixels ellipse(175, 60, 90, 90);
        ellipse(390, 390, 90, 90);
        rect(390,10,1,400)
  strokeWeight(20);  // Stroke weight to 20 pixels
        ellipse(400, 10, 90, 90);
rect(10,390, 400, 1);
        ellipse(10,390,90,90);
        rect(7,7,400,1)
  }
